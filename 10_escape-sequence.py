# escape characters
# \\ backlash
# \' single quote
# \" double quote
# \a ASCII bell
# \b ASCII backspace
# \f ASCII formfeed
# \n ASCII linefeed
# \N{name} Character named name in Unicode database
# \r carriage return
# \t horizontal tab
# \uxxxx character with 16bit hex value
# \Uxxxxxxxx character with 32bit hex value
# \v ASCII vertical tab
# \ooo character with octal value ooo
# \xhh character with hex value hh